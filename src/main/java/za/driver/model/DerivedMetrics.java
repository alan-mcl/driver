package za.driver.model;

public class DerivedMetrics {

    private Double safetyScore;
    private Double runningCostScore;
    private Double reliabilityScore;
    private Double comfortScore;
    private Double performanceScore;
    private Double dailyDriverScore;
    private Double technologyScore;
    private Double prestigeScore;
    private Double awesomenessScore;
    private Integer reliabilityConfidence;
    private Double overallScore;
    private Double scorePer100k;

    public DerivedMetrics() {
    }

    public Double getSafetyScore() {
        return safetyScore;
    }

    public void setSafetyScore(Double safetyScore) {
        this.safetyScore = safetyScore;
    }

    public Double getRunningCostScore() {
        return runningCostScore;
    }

    public void setRunningCostScore(Double runningCostScore) {
        this.runningCostScore = runningCostScore;
    }

    public Double getReliabilityScore() {
        return reliabilityScore;
    }

    public void setReliabilityScore(Double reliabilityScore) {
        this.reliabilityScore = reliabilityScore;
    }

    public Double getComfortScore() {
        return comfortScore;
    }

    public void setComfortScore(Double comfortScore) {
        this.comfortScore = comfortScore;
    }

    public Double getPerformanceScore() {
        return performanceScore;
    }

    public void setPerformanceScore(Double performanceScore) {
        this.performanceScore = performanceScore;
    }

    public Double getDailyDriverScore() {
        return dailyDriverScore;
    }

    public void setDailyDriverScore(Double dailyDriverScore) {
        this.dailyDriverScore = dailyDriverScore;
    }

    public Double getTechnologyScore() {
        return technologyScore;
    }

    public void setTechnologyScore(Double technologyScore) {
        this.technologyScore = technologyScore;
    }

    public Double getPrestigeScore() {
        return prestigeScore;
    }

    public void setPrestigeScore(Double prestigeScore) {
        this.prestigeScore = prestigeScore;
    }

    public Double getAwesomenessScore() {
        return awesomenessScore;
    }

    public void setAwesomenessScore(Double awesomenessScore) {
        this.awesomenessScore = awesomenessScore;
    }

    public Integer getReliabilityConfidence() {
        return reliabilityConfidence;
    }

    public void setReliabilityConfidence(Integer reliabilityConfidence) {
        this.reliabilityConfidence = reliabilityConfidence;
    }

    public Double getOverallScore() {
        return overallScore;
    }

    public void setOverallScore(Double overallScore) {
        this.overallScore = overallScore;
    }

    public Double getScorePer100k() {
        return scorePer100k;
    }

    public void setScorePer100k(Double scorePer100k) {
        this.scorePer100k = scorePer100k;
    }
}
