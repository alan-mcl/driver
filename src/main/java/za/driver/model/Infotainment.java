package za.driver.model;

public class Infotainment {

    private Double infotainmentScreenSizeInches;
    private Integer speakerCount;

    public Infotainment() {
    }

    public Double getInfotainmentScreenSizeInches() {
        return infotainmentScreenSizeInches;
    }

    public void setInfotainmentScreenSizeInches(Double infotainmentScreenSizeInches) {
        this.infotainmentScreenSizeInches = infotainmentScreenSizeInches;
    }

    public Integer getSpeakerCount() {
        return speakerCount;
    }

    public void setSpeakerCount(Integer speakerCount) {
        this.speakerCount = speakerCount;
    }
}
